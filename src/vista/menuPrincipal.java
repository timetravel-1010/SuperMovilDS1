    public void pintarPanelActual(JPanel panel){
        panelActual=panel;
        Dimension dim = panel.getPreferredSize();
        this.getContentPane().add(panelActual, new org.netbeans.lib.awtextra.AbsoluteConstraints(160, 60, dim.width, dim.height));
        //this.getContentPane().add(panelActual, new org.netbeans.lib.awtextra.AbsoluteConstraints(160, 60, 1110, 650));
        this.revalidate();
        this.repaint();
    }
    
    public void eliminarPanelActual(){
        this.getContentPane().remove(panelActual);
        this.revalidate();
        this.repaint();
    }
    
    public void refrescarGUI(){
            this.revalidate();
            this.repaint();
        }
    
    public void terminarSesion(){
        int opcion = JOptionPane.showConfirmDialog(this, "¿Está seguro que desea terminar la sesión?", "Cerrando sesión...", JOptionPane.YES_NO_OPTION);
        if(opcion==0){
        conexion.updateStatus_login(usuarioLogueado.getCedula(), false);
        login.setVisible(true);
        this.dispose();
        }
    }
    
    public void cerrarPrograma() {
        int opcion = JOptionPane.showConfirmDialog(this, "¿Seguro que desea cerrar el programa?", "Cerrando programa...", JOptionPane.YES_NO_OPTION);
        if (opcion == 0) {
            conexion.updateStatus_login(usuarioLogueado.getCedula(), false);
            this.dispose();
            System.exit(0);
        }
    }
    
