    public menuPrincipal(ConnectionDB conex, Login login) {
        this.conexion = conex;
        this.login = login;
        this.usuarioLogueado = conexion.getUsuarioOnline();
        initComponents();
        getContentPane().setBackground(new java.awt.Color(218, 234, 255));
        this.setIconImage(new ImageIcon(getClass().getResource("/vista/img/icono.png")).getImage());
        this.addWindowListener(new WindowAdapter(){
            public void windowClosing(WindowEvent arg0) {
                cerrarPrograma();
            }
        });
        this.setVisible(true);
        nombreUsuario = usuarioLogueado.getNombre();
        rol = usuarioLogueado.getRol();
        fechaSesion = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss").format(Calendar.getInstance().getTime());
        
        labelUsuario.setText(nombreUsuario);
        labelRol.setText(rol);
        labelFecha.setText(fechaSesion);
        
        this.repaint();
        if(rol.equals("administrador")){
            buttonUsuarios.setEnabled(true);
        } else if(rol.equals("gerente")){
            buttonReportes.setEnabled(true);
        } else if(rol.equals("operador")){
            buttonClientes.setEnabled(true);
            buttonPagos.setEnabled(true);
            buttonFacturacion.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(this, "Algo raro ha sucedido");
        }
        this.repaint();
        
    }

